                        дополнительные расходы).
                    </p>

                    <h3 class="content-subtitle accent">Наследство без завещания — наследование по закону</h3>

                    <p class="text-paragraph">
                        Поскольку о создании наследственного фонда должно быть указано в завещании, то в случае его
                        отсутствия (или если нарушены права наследников упомянутых нами ранее) в силу вступает действие
                        закона. Наследственная масса распределяется в порядке очередности (родства), установленной
                        семейным кодексом РФ. Эта очередность состоит из восьми пунктов.
                    </p>

                    <ol class="numbered-list">
                        <li>Первая очередь — самые близкие родственники. Родитель/ли, супруг/супруга, ребенок/дети или
                            их внуки (если детей нет в живых).</li>
                        <li>Вторая очередь – брат, сестра (в том числе сводные), племянник/племянница.</li>
                        <li>Третья очередь – дядя, тетя, их дети.</li>
                        <li>Иные лица, имеющие с наследодателем родственную кровную связь (4-6 очереди).</li>
                        <li>Сюда относится – пасынок, падчерица, иные не кровные родственники (7 очередь).</li>
                        <li>При отсутствии наследников первых семи очередей, наследство передается в пользу государства
                            (8 очередь).</li>
                    </ol>

