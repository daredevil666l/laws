                    <h2 class="content-title">Как получить наследство:</h2>

                    <p class="text-paragraph">
                        В Российском законодательстве укрепилось (то есть вступило в законную силу) такое понятие, как
                        наследственный фонд. Его создание и функционирование возможно только после смерти наследодателя.
                    </p>

                    <p class="text-paragraph">
                        Безусловно, размер наследственного фонда, его условия будут строго соответствовать позиции
                        изложенной в завещании, при его наличии. К такому фонду (или наследникам) кредиторы усопшего
                        могут обратиться с требованием о его погашении. Кроме того под защитой фонда будут находиться
                        права не достигших совершеннолетия наследников, даже при их отсутствии в завещании. Помимо этого
                        кроме законных наследников наследственная масса может быть распределена на благотворительность
                        по воле наследодателя. Это устанавливает попечительский совет. Что такое или кто такой
                        наследственный фонд. Это своеобразное юридическое образование, созданное с целью
                        распоряжения/распределения денежной массы, бизнеса, иных активов отраженных в комментируемом
                        юридическом документе. Создается он по воле наследодателя, отраженной письменно, исключительно в
                        связи с его смертью.
                    </p>

                    <h3 class="content-subtitle">Как вступить в наследство по завещанию. Может ли вступить в наследство
                        не родственник?</h3>

                    <p class="text-paragraph">
                        Как и прежде в завещании может быть указано любое лицо, а оно может быть и юридическим, по
                        усмотрению составителя документа, в том числе не имеющее родственных отношений с завещателем.
                        Имеют ли право на наследство иностранные граждане? Если они указаны в завещании – да. Однако
                        если в документе не упомянуты дети, не достигшие совершеннолетия или признанные
                        нетрудоспособными дети, один или оба родителя, законные муж или жена, лица находящиеся на
                        иждивении – они являются наследниками по закону вне зависимости от того, распределено ли на них
                        имущество или нет. Безусловно, за наследодателем сохраняется право лишения наследства
                        родственников (не упомянутых выше).
                    </p>

                    <h3 class="content-subtitle">Какое имущество может быть передано по наследству</h3>

                    <p class="text-paragraph">
                        В наследственную массу может быть включено как имущество в натуральном выражении (квартира,
                        машина, земля), так и имущественное право. К примеру, право (точнее сказать обязанность) на
                        выплату кредитных обязательств в отношении наследуемого имущества. Кроме того любое завещание
                        может содержать сведения о правопреемнике наследства (на случай погребения первично выбранного
                        наследника или если он воспользуется правом на отказ от него). Одно и то же имущество может быть
                        распределено (у каждого наследника есть право доли на наследство) между несколькими
                        наследниками. То есть наследство можно поделить. Уже с 01.06.2018 года супруги могут составить
                        одно единое завещание. Сам документ может быть удостоверен нотариусом, но и может быть составлен
                        в его отсутствии и передан ему в закрытом конверте, при двух свидетелях. В случае если у
                        наследодателя имеются соответствующие объективные причины (угроза жизни и здоровью), завещание
                        составляется и подписывается лично. Два свидетеля также должны подтвердить этот факт.
                    </p>

